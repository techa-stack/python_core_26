# >  : greater than
# <  : less than
# >= :  either greater than or equal to
# <= : either less than or equal to
# != : not equal to
# == : equal to

# O/P : Always a boolean value. True/False

# left_operand <RO> right operand
# eg. x > y

op = 10 > 5 # True/ False
print(op)

op = 10 < 5 # True/ False
print(op)

num1, num2, num3, num4 = 1, 2, 3, 2

print(num1 > num2)
print(num1 < num2)
print(num1 >= num4)
print(num3 <= num1)
print(num2 <= num4)
print(num1 != num2)
print(num2 != num4)
print(num2 == num4)

op = num1 = num2 #Assignment operator
print(op)