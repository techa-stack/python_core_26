num1, num2, num3, num4 = 20, 30, 5, 2

sum = num1 + num2 # addition
diff = num1 - num2 # subtraction
mul = num1 * num3 # multiplication
print(sum)
print(diff)
print(mul)

# Division =>  / : Float division     // : Integer Division
div_1 = num3 / num4  # float div
div_2 = num3 // num4 # integer div
div_3 = num1 / num3
div_4 = num1 // num3

print(div_1)
print(div_2)
print(div_3)
print(div_4)

# num1, num2, num3, num4 = 20, 30, 5, 2
exp = num3 ** num4  # 5 ** 2 : 2 to the power 5, 2 raised to 5
print(exp)

rem = num3 % num4 # 5 % 2
print(rem)

rem = num4 % num3 # 2 % 5
print(rem)


